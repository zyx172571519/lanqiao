package org.taru.lanqiao.model;

public class Other_Data {
	private String data_id;
	
	private String data_name;
	
	private String data_value;
	
	private String data_product_id;
	
	public String getData_id() {
		return data_id;
	}
	public void setData_id(String data_id) {
		this.data_id = data_id;
	}
	public String getData_name() {
		return data_name;
	}
	public void setData_name(String data_name) {
		this.data_name = data_name;
	}
	public String getData_value() {
		return data_value;
	}
	public void setData_value(String data_value) {
		this.data_value = data_value;
	}
	public String getData_product_id() {
		return data_product_id;
	}
	public void setData_product_id(String data_product_id) {
		this.data_product_id = data_product_id;
	}
	
}
